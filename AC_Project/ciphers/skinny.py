# =========================================================
# CIPHER 12 — SKINNY-128-128  (Beierle et al., CRYPTO 2016)
# =========================================================
# Tweakable SPN. 128-bit block, 128-bit tweakey (key only, no tweak here).
# State: 4×4 byte array (16 bytes).
#
# Round function (repeated `rounds` times):
#   SubCells    : 8-bit S-box applied to each state byte
#   AddConstants: XOR 6-bit LFSR round constant into bytes 0, 4, 8
#   AddRoundKey : XOR first 8 bytes of tweakey into state rows 0–1
#   ShiftRows   : row i rotated left by i positions (same direction as AES)
#   MixColumns  : SKINNY-specific linear layer (NOT the AES MixColumns)
#                 Matrix M over GF(2):
#                   row0' = row0 ^ row2 ^ row3
#                   row1' = row0
#                   row2' = row1 ^ row2
#                   row3' = row0 ^ row2
#   Key schedule: permute all 16 tweakey cells by PT each round
#
# PT permutation (cell indices 0..15, row-major):
#   [9, 15, 8, 13, 10, 14, 12, 11, 0, 1, 2, 3, 4, 5, 6, 7]
#
# S-box: SKINNY-128-specific 8-bit S-box (NOT the AES S-box).
#        It is a bijection on {0..255} — verified in _skinny_unit_tests().
#
# Reference: Beierle et al., "The SKINNY Family of Block Ciphers and
#            Its Low-Latency Variant MANTIS", CRYPTO 2016, LNCS 9815.
# =========================================================

