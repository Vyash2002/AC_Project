# Tweakey cell permutation applied every round to all 16 cells (row-major)
_SKINNY_PT = [9, 15, 8, 13, 10, 14, 12, 11, 0, 1, 2, 3, 4, 5, 6, 7]


def skinny128_encrypt(P: bytes, rounds: int, key: bytes, **_) -> bytes:
    """
    SKINNY-128-128 reduced-round encryption.

    Parameters
    ----------
    P      : 16-byte plaintext
    rounds : number of rounds to apply (1 ≤ rounds ≤ 40 per spec)
    key    : 16-byte key (used as the full 128-bit tweakey; no tweak)

    Returns
    -------
    16-byte ciphertext (state bytes in row-major order)

    Implementation notes
    --------------------
    Round constants are generated by a 6-bit LFSR with initial value 1.
    Feedback polynomial: bit5 XOR bit4, shifted in at LSB.
    The constant is split across column 0:
        byte 0  ← rc6[3:0]   (lower 4 bits)
        byte 4  ← rc6[5:4]   (upper 2 bits)
        byte 8  ← 0x02       (always 2, as per spec)

    MixColumns applies column-by-column to the 4×4 state:
        new_r0 = r0 ^ r2 ^ r3
        new_r1 = r0
        new_r2 = r1 ^ r2
        new_r3 = r0 ^ r2
    where r0..r3 are the bytes at rows 0–3 of the current column.
    """
    state = list(P)      # 16 bytes, row-major 4×4
    tk    = list(key)    # 16-byte tweakey

    rc6 = 1              # 6-bit LFSR; spec initialises to 1

    for _ in range(rounds):

        # ── SubCells ──────────────────────────────────────────────────────
        state = [_SKINNY128_SBOX[b] for b in state]

        # ── AddConstants ──────────────────────────────────────────────────
        # Split rc6 into two nibble-level constants and XOR into column 0
        c0 =  rc6       & 0x0F   # bits [3:0]
        c1 = (rc6 >> 4) & 0x03   # bits [5:4]
        state[0] ^= c0
        state[4] ^= c1
        state[8] ^= 0x02

        # Advance LFSR: feedback = bit5 XOR bit4, shift left by 1
        feedback = ((rc6 >> 5) ^ (rc6 >> 4)) & 1
        rc6 = ((rc6 << 1) | feedback) & 0x3F

        # ── AddRoundKey ───────────────────────────────────────────────────
        # Only rows 0 and 1 of the tweakey are XOR'd into the state
        for i in range(8):
            state[i] ^= tk[i]

        # ── ShiftRows ─────────────────────────────────────────────────────
        # Row 0 (bytes 0–3)  : no change
        # Row 1 (bytes 4–7)  : cyclic left shift by 1
        state[4], state[5], state[6], state[7] = \
            state[5], state[6], state[7], state[4]
        # Row 2 (bytes 8–11) : cyclic left shift by 2
        state[8],  state[9],  state[10], state[11] = \
            state[10], state[11], state[8],  state[9]
        # Row 3 (bytes 12–15): cyclic left shift by 3
        state[12], state[13], state[14], state[15] = \
            state[15], state[12], state[13], state[14]

        # ── MixColumns ────────────────────────────────────────────────────
        # Applied independently to each of the 4 columns
        for c in range(4):
            r0 = state[c]
            r1 = state[4  + c]
            r2 = state[8  + c]
            r3 = state[12 + c]
            state[c]      = r0 ^ r2 ^ r3
            state[4  + c] = r0
            state[8  + c] = r1 ^ r2
            state[12 + c] = r0 ^ r2

        # ── Key schedule ──────────────────────────────────────────────────
        tk = [tk[_SKINNY_PT[i]] for i in range(16)]

    return bytes(state)